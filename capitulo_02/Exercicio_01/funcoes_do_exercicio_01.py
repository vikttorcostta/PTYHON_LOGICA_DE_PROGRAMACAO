# ARQUIVO DE FUNÇÕES

# LETRA A
def letra_a(c, r):
    f = (((1 * c) / 1) - ((4 * c) ** 2) / (r ** 2)) ** 2
    return f


# LETRA B
def letra_b(b, c, a):
    x = (b-c) / a
    return x


# LETRA C
def letra_c(m, a, h, v):
    x = m * ((a * h) + (v ** 2) / 2)
    return x


# LETRA D
def letra_d(v, t, a):
    d = (v * t) + ((a * t) ** 2) / 2
    return d


# LETRA E
def letra_e(p, r, n):
    d = (p - r) / n
    return d


# LETRA F
def letra_f(c):
    f = (c * (9 / 5)) + 32
    return f


# LETRA E
def letra_e(f):
    c = (f - 32) * (5 / 9)
    return c









